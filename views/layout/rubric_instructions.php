            <u><h3>Key Definitions</h3></u>
            <div class="keyDefinitions">
                <p><strong>Program:</strong> Start block + any middle block. Can include end blocks as well but not
                    necessary to be counted as a program.</p>
                <p><strong>Correctly:</strong> Blocks used "correctly" means that the block made a change to the
                    project.</p>
                <p><i>Examples of incorrect usage:</i></p>
                <ul class="blocksList">
                    <li><strong class="startBlocks">Start blocks:</strong>
                        <ul>
                            <li>Using a "Start on bump" if no other characters are present on the page.</li>
                            <li>Using "Start on message" without "Send message" in the corresponding color or vice
                                versa.
                            </li>
                        </ul>
                    </li>
                    <li><strong class="motionBlocks">Motion blocks:</strong> Using "Go to home" when nothing has made
                        the character move or change.
                    </li>
                    <li><strong class="looksBlocks">Looks blocks:</strong> Using a "Reset size" or "Appear" if the
                        character hasnâ€™t changed size or disappeared.
                    </li>
                    <li><strong class="soundBlocks">Sound blocks:</strong> No way to use sound blocks incorrectly.</li>
                    <li><strong class="controlBlocks">Control blocks:</strong>
                        <ul>
                            <li><strong>Wait:</strong> Adding a wait as the last block of a program (or with a plain red
                                end after).
                            </li>
                            <li><strong>Stop:</strong> Having a stop block if there are no other programs in the
                                programming area.
                            </li>
                            <li><strong>Set speed:</strong> Using "Set speed" without any motion blocks present.</li>
                            <li><strong>Repeat loop:</strong>
                                <ul>
                                    <li>Having an empty repeat loop.</li>
                                    <li>Using a repeat loop with a parameter of 1.</li>
                                    <li>Using a repeat loop over all middle blocks in the program if a "Repeat forever"
                                        end block is already used.
                                    </li>
                                </ul>
                            </li>
                        </ul>
                    </li>
                    <li><strong class="endBlocks">End block:</strong> No way to use end blocks incorrectly.</li>
                </ul>
                <p><strong>Program with multiple blocks:</strong> Start block + more than one middle block or at least
                    one middle block + not the plain end block (i.e., an end block that makes a change to the program -
                    "Repeat forever" or "Go to page").</p>
                <p><strong>Theme:</strong> Continuity across an element of the project. Elements of the project include
                    programs, characters, pages, or visual customization.</p>
            </div>

